    private void btnAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltaActionPerformed
        if (validarDatos()) {
            Cliente c = new Cliente();
            c.setDni(txtDNI.getText());
            c.setNombre(txtNombre.getText());
            c.setApellidos(txtApellidos.getText());
            c.setDireccion(txtDirecion.getText());
            c.setTelefono(txtTlf.getText());
            c.setCorreo(txtCorreo.getText());
            c.setObservaciones(txtaObs.getText());
            c.setRuta_foto(fotoPerfil);

            String mensaje = "";
            switch (GestionBD.altaCliente(c)) {
                case 1:
                    mensaje = "Usuario " + c.getNombre() + " dado de alta.";
                    ((Main)getParent().getParent().getParent().getParent().getParent()).recargarHijos();
                    break;
                case 0:
                    mensaje = "Ya existe un usuario con DNI igual a " + c.getDni();
                    break;
                case -1:
                    mensaje = "Sea ha producido un error";
                    break;
                case -2:
                    mensaje = "Sea ha producido un error al crear la mensualidad correspondiente al mes actual";
                    break;
            }
            JOptionPane.showMessageDialog(this, mensaje);
        } else {
            JOptionPane.showMessageDialog(this, "Es necesario introducir los datos");
        }
    }//GEN-LAST:event_btnAltaActionPerformed

    private void mniSeleccionIMGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniSeleccionIMGActionPerformed
        JFileChooser jfc = new JFileChooser();
        jfc.setMultiSelectionEnabled(false);
        String extensions[] = {"jpg", "jpeg", "png"};
        jfc.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true;
                } else {
                    String path = f.getAbsolutePath().toLowerCase();
                    for (int i = 0, n = extensions.length; i < n; i++) {
                        String extension = extensions[i];
                        if ((path.endsWith(extension) && (path.charAt(path.length() - extension.length() - 1)) == '.')) {
                            return true;
                        }
                    }
                }
                return false;
            }

            @Override
            public String getDescription() {
                return "JPG, JPEG, PNG";
            }
        });

        int i = jfc.showOpenDialog(this);
        if (i == JFileChooser.APPROVE_OPTION) {
            fotoPerfil = "./src/Imagenes/" + jfc.getSelectedFile().getName();
            if (archivoRepetido(jfc.getSelectedFile().getName())) {
                JOptionPane.showMessageDialog(this, "Ya existe una imagen con ese nombre");
                return;
            }
            try (InputStream is = new FileInputStream(jfc.getSelectedFile());
                    OutputStream os = new FileOutputStream(new File(fotoPerfil))) {

                byte[] buffer = new byte[1024];
                int length;
                while ((length = is.read(buffer)) > 0) {
                    os.write(buffer, 0, length);
                }
                ImageIcon ii = new ImageIcon(fotoPerfil);
                Image image = ii.getImage();
                Image newimg = image.getScaledInstance(90, 100, java.awt.Image.SCALE_SMOOTH);
                ii = new ImageIcon(newimg);
                lblImg.setIcon(ii);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error con imagen: " + ex);
            }
        } else {
            ImageIcon ii = new ImageIcon("./src/Imagenes/spartan_default_img.png");
            Image image = ii.getImage();
            Image newimg = image.getScaledInstance(90, 100, java.awt.Image.SCALE_SMOOTH);
            ii = new ImageIcon(newimg);
            lblImg.setIcon(ii);
        }
    }//GEN-LAST:event_mniSeleccionIMGActionPerformed

